"""
AWS CloudWatch Webhook Receiver

Accepts AWS SNS/CloudWatch alarm notifications and feeds them directly
into the full AI agent pipeline — no email needed.

HOW TO CONNECT IN AWS:
  1. Go to AWS SNS → Create Topic → Standard
  2. Create Subscription → Protocol: HTTPS
  3. Endpoint: https://YOUR_SERVER/api/webhook/aws
  4. Your server auto-confirms the subscription
  5. Go to CloudWatch → Alarms → select alarm → Actions → Send to SNS topic

PAYLOAD TYPES HANDLED:
  - SubscriptionConfirmation  (auto-confirms so AWS knows endpoint is valid)
  - Notification              (actual CloudWatch alarm firing)
  - UnsubscribeConfirmation   (graceful unsubscribe)

TEST WITHOUT AWS:
  POST /api/webhook/aws/test  (simulates a P1 CPU alarm)
"""

import json
import logging
import asyncio
import uuid as _uuid
from datetime import datetime
from typing import Any, Dict
from fastapi import APIRouter, Request, BackgroundTasks, HTTPException
from fastapi.responses import JSONResponse

logger  = logging.getLogger(__name__)
router  = APIRouter()

# Track last 20 webhook events for the dashboard
_webhook_log: list = []


def _log_event(source: str, alarm_name: str, state: str, detail: str) -> None:
    """Store webhook event for dashboard display."""
    _webhook_log.insert(0, {
        "id":         str(_uuid.uuid4())[:8],
        "source":     source,
        "alarm_name": alarm_name,
        "state":      state,
        "detail":     detail[:120],
        "received_at": datetime.now().strftime("%H:%M:%S"),
    })
    if len(_webhook_log) > 20:
        _webhook_log.pop()


def _cloudwatch_to_email(alarm: Dict[str, Any]) -> str:
    """
    Convert a CloudWatch alarm payload into an email-like text
    that the AI pipeline understands.
    """
    alarm_name   = alarm.get("AlarmName",        "Unknown Alarm")
    description  = alarm.get("AlarmDescription", "No description")
    state        = alarm.get("NewStateValue",    "ALARM")
    reason       = alarm.get("NewStateReason",   "")
    region       = alarm.get("Region",           "unknown region")
    state_time   = alarm.get("StateChangeTime",  "")

    # Extract metric info
    trigger      = alarm.get("Trigger", {})
    metric       = trigger.get("MetricName", "Unknown Metric")
    namespace    = trigger.get("Namespace",  "Unknown Service")
    threshold    = trigger.get("Threshold",  "N/A")
    dimensions   = trigger.get("Dimensions", [])
    dim_str      = ", ".join(
        f"{d.get('name','?')}={d.get('value','?')}" for d in dimensions
    ) or "N/A"

    # Determine severity
    priority_hint = ""
    name_lower    = alarm_name.lower()
    if any(w in name_lower for w in ["critical", "p1", "down", "outage", "crash"]):
        priority_hint = "CRITICAL PRIORITY — P1"
    elif any(w in name_lower for w in ["high", "p2", "error", "fail"]):
        priority_hint = "HIGH PRIORITY — P2"
    else:
        priority_hint = "MEDIUM PRIORITY — P3"

    return f"""From: AWS CloudWatch <cloudwatch@aws.amazon.com>
Subject: AWS ALARM: {alarm_name} — {state}
X-Source: aws-cloudwatch
X-Alarm-State: {state}

{priority_hint}

AWS CloudWatch Alarm Triggered
================================
Alarm Name  : {alarm_name}
Description : {description}
State       : {state}
Region      : {region}
Time        : {state_time}

Metric Details:
  Metric    : {metric}
  Namespace : {namespace}
  Threshold : {threshold}
  Dimensions: {dim_str}

Reason:
{reason}

Please investigate and resolve this alarm immediately.
This alert was automatically generated by AWS CloudWatch
and forwarded to the AI Operations Center for processing.
"""


async def _run_pipeline(raw_text: str, alarm_name: str, state: str) -> None:
    """Run the full AI agent pipeline on the alarm — in background."""
    from app.agents.manager import build_manager_graph
    from app.core.results_store import store_result
    from app.services.email_reply import send_reply
    import time as _time

    subject = f"AWS ALARM: {alarm_name} — {state}"
    sender  = "cloudwatch@aws.amazon.com"

    graph        = build_manager_graph()
    thread_id    = str(_uuid.uuid4())
    initial_state = {
        "raw_email":         raw_text,
        "email_data":        {},
        "workflow_plan":     {},
        "incident_result":   {},
        "knowledge_results": {},
        "ticket_result":     {},
        "meeting_result":    {},
        "executive_summary": "",
        "human_approved":    False,
    }
    config = {"configurable": {"thread_id": thread_id}}

    try:
        t0        = _time.monotonic()
        result    = await graph.ainvoke(initial_state, config=config)
        elapsed   = int((_time.monotonic() - t0) * 1000)

        reply_sent = False
        try:
            reply_sent = await send_reply(
                original_sender=sender,
                original_subject=subject,
                result=result or {},
                uid=f"webhook-aws-{thread_id}",
            )
        except Exception as re:
            logger.warning(f"[Webhook/AWS] Reply failed: {re}")

        store_result(subject, sender, result or {},
                     process_ms=elapsed, reply_sent=reply_sent)

        # Post-mortem for P1
        priority = (result or {}).get("email_data", {}).get("priority", "")
        if priority == "P1-critical":
            try:
                from app.services.pdf_postmortem import send_postmortem
                asyncio.create_task(
                    send_postmortem(subject=subject, result=result or {})
                )
            except Exception:
                pass

        logger.info(
            f"[Webhook/AWS] ✅ Pipeline done: {subject[:60]} | "
            f"reply={'✅' if reply_sent else '❌'} | {elapsed}ms"
        )

    except Exception as e:
        logger.error(f"[Webhook/AWS] Pipeline failed: {e}")


