@router.post("/webhook/aws")
async def aws_cloudwatch_webhook(
    request:          Request,
    background_tasks: BackgroundTasks,
) -> JSONResponse:
    """
    Main AWS SNS/CloudWatch webhook endpoint.
    AWS requires a 200 response within 15 seconds — pipeline runs in background.
    """
    # Parse body — AWS sends JSON
    try:
        body = await request.json()
    except Exception:
        raw = await request.body()
        logger.warning(f"[Webhook/AWS] Could not parse JSON: {raw[:200]}")
        return JSONResponse({"status": "ignored", "reason": "invalid json"})

    msg_type = body.get("Type", "")
    logger.info(f"[Webhook/AWS] Received SNS message type: {msg_type}")

    # ── Handle subscription confirmation ─────────────────────────────────────
    if msg_type == "SubscriptionConfirmation":
        subscribe_url = body.get("SubscribeURL", "")
        if subscribe_url:
            try:
                import httpx
                async with httpx.AsyncClient(timeout=10) as client:
                    await client.get(subscribe_url)
                logger.info("[Webhook/AWS] ✅ SNS subscription confirmed automatically")
                _log_event("AWS SNS", "Subscription", "CONFIRMED",
                           "SNS subscription confirmed — AWS is now connected")
            except Exception as e:
                logger.warning(f"[Webhook/AWS] Auto-confirm failed: {e}")
        return JSONResponse({"status": "confirmed"})

    # ── Handle actual alarm notification ─────────────────────────────────────
    if msg_type == "Notification":
        subject     = body.get("Subject", "AWS CloudWatch Notification")
        raw_message = body.get("Message", "{}")

        # Message is a JSON string inside the SNS envelope
        try:
            alarm = json.loads(raw_message)
        except json.JSONDecodeError:
            alarm = {"AlarmName": subject, "NewStateValue": "ALARM",
                     "NewStateReason": raw_message}

        alarm_name = alarm.get("AlarmName",     subject)
        state      = alarm.get("NewStateValue", "ALARM")
        reason     = alarm.get("NewStateReason","")

        logger.warning(f"[Webhook/AWS] 🚨 Alarm: {alarm_name} → {state}")
        _log_event("AWS CloudWatch", alarm_name, state, reason)

        # Only process ALARM state (not OK recovery)
        if state == "ALARM":
            raw_email = _cloudwatch_to_email(alarm)
            background_tasks.add_task(_run_pipeline, raw_email, alarm_name, state)
            return JSONResponse({
                "status":     "processing",
                "alarm":      alarm_name,
                "state":      state,
                "message":    "AI pipeline triggered in background",
            })
        else:
            # State is OK — alarm resolved
            logger.info(f"[Webhook/AWS] ✅ Alarm resolved: {alarm_name}")
            # Mark resolved in SQLite
            try:
                from app.core.db import mark_resolved
                # best-effort — alarm name match
            except Exception:
                pass
            return JSONResponse({
                "status":  "ok",
                "alarm":   alarm_name,
                "message": "Alarm resolved — no pipeline needed",
            })

    return JSONResponse({"status": "ignored", "type": msg_type})


@router.get("/webhook/events")
async def get_webhook_events():
    """Returns recent webhook events for dashboard display."""
    return {"events": _webhook_log}


